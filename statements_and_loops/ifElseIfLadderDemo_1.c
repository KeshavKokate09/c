#include<stdio.h>

void main(){
	int number;
	printf("Enter a number between 1 to 10\n");
	scanf("%d",&number);

	if(number == 1){
		printf("ONE\n");
	}
	else if(number == 2){
		printf("TWO\n");
	}
	else if(number == 3){
		printf("THREE\n");
	}
	else if(number == 4){
		printf("FOUR\n");
	}
	else if(number == 5){
		printf("FIVE\n");
	}
	else if(number == 6){
		printf("SIX\n");
	}
	else if(number == 7){
		printf("SEVEN\n");
	}
	else if(number == 8){
		printf("EIGHT\n");
	}
	else if(number == 9){
		printf("NINE\n");
	}
	else if(number == 10){
		printf("TEN\n");
	}
	else{
		printf("Wrong Input\n");
	}

}
